"""2."""

header = lines_rdd.first()
data = lines_rdd.filter(lambda line: line != header)

parsed = data.map(lambda line: line.split(","))

def extract_origin_delay(row):
    #ORIGIN_AIRPORT
    origin = row[3]

    #DEP_DELAY
    delay_str = row[6]
    if delay_str == '' or delay_str is None:
       delay = 0.0
    else:
      try:
          delay = float(delay_str)
      except ValueError:
          delay = 0.0

    return (origin,delay)

# Δημιουργούμε το ζεύγος (origin_airport, delay_minutes)
origin_delay = parsed.map(extract_origin_delay)

print("-Pairs(Origin,Delay)-")
for pair in origin_delay.take(5):
    print(pair)

"""3."""

# Μεση καθυστέρηση ανα αεροδρόμιο
def create_comb(v):
    return (v, 1)

def merge_val(acc, v):
    # προσθέτει νέα τιμή σε υπάρχον accumulator
    total_delay = acc[0] + v
    count = acc[1] + 1
    return (total_delay, count)

def merge_comb(acc1, acc2):
    # συγχωνεύει accumulators από διαφορετικά partitions
    total_delay = acc1[0] + acc2[0]
    total_count = acc1[1] + acc2[1]
    return (total_delay, total_count)


execution_times = []
for i in range(5):
    start_time = time.time()  # Ξεκινάει η Μέτρηση χρόνου εκτέλεσης

    avg_rdd = (origin_delay.combineByKey(create_comb, merge_val, merge_comb).mapValues(lambda acc: acc[0] / acc[1]))

    temp_result = avg_rdd.sortBy(lambda x: x[1], ascending=False).take(20)

    end_time = time.time()   # Τελειώνει η Μέτρηση χρόνου εκτέλεσης
    elapsed_time = end_time - start_time
    execution_times.append(elapsed_time)
    print(f"Επανάληψη {i+1}: {elapsed_time:.4f} δευτερόλεπτα")


# Διαδικασία Υπολογισμου του μέσου όρου των 3 (-min, -max) μετρήσεων
times_array = np.array(execution_times)
times_array = np.sort(times_array)
trimmed_times = times_array[1:-1]

avg_rdd_time = np.mean(trimmed_times)

print(f"\nΣύνολο Μετρήσεων: {execution_times}")
print(f"Αφαιρούμενες Τιμές (min/max): {times_array[0]:.4f} / {times_array[-1]:.4f}")
print(f"Μέσος Χρόνος Θέματος 1 (RDD - Trimmed Mean): {avg_rdd_time:.4f} δευτερόλεπτα")

results = avg_rdd.sortBy(lambda x: x[1], ascending=False).collect()
print("\n-Μέση καθυστέρηση ανα αεροδρόμιο-")
for result in results:
    print(f"Airport: {result[0]}, Average Delay: {result[1]:.2f} minutes")

"""4."""


from pyspark.sql.functions import col, avg, desc

df_clean = df_flights.filter(col("CANCELLED") == 0) \
             .na.drop(subset=["DEP_DELAY"])

route_delays = df_clean.groupBy("ORIGIN_AIRPORT", "DEST_AIRPORT") \
                          .agg(avg("DEP_DELAY").alias("AVG_DELAY"))

top_routes = route_delays.orderBy(desc("AVG_DELAY"))


